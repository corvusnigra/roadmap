/** Координаты узла на canvas по уровню и индексу внутри уровня. */
export function vibecodingPosition(
  level: number,
  indexInLevel: number,
): { x: number; y: number } {
  return { x: level * 360, y: indexInLevel * 130 };
}

/**
 * Prereq-цепочка: линейная внутри уровня, мост между уровнями
 * (первая дисциплина Lk = после последней L(k-1)).
 */
export function buildVibecodingNodeSeeds(): {
  slug: string;
  title: string;
  summary: string;
  positionX: number;
  positionY: number;
  estimatedMinutes: number;
  prerequisites: string[];
}[] {
  const byLevel = new Map<number, LDiscipline[]>();
  for (const d of VIBECODING_DISCIPLINES) {
    const bucket = byLevel.get(d.level) ?? [];
    bucket.push(d);
    byLevel.set(d.level, bucket);
  }

  const out: {
    slug: string;
    title: string;
    summary: string;
    positionX: number;
    positionY: number;
    estimatedMinutes: number;
    prerequisites: string[];
  }[] = [];

  let prevLevelLast: string | null = null;
  for (const lvl of [0, 1, 2, 3, 4] as const) {
    const disciplines = byLevel.get(lvl) ?? [];
    let prevInLevel: string | null = prevLevelLast;
    disciplines.forEach((d, idx) => {
      const pos = vibecodingPosition(d.level, idx);
      out.push({
        slug: d.slug,
        title: d.title,
        summary: d.question,
        positionX: pos.x,
        positionY: pos.y,
        estimatedMinutes: d.estimatedMinutes,
        prerequisites: prevInLevel ? [prevInLevel] : [],
      });
      prevInLevel = d.slug;
    });
    const last = disciplines[disciplines.length - 1];
    if (last) prevLevelLast = last.slug;
  }

  return out;
}
