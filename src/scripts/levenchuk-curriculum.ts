/** Возвращает координаты узла на canvas по уровню и индексу внутри уровня. */
export function disciplinePosition(
  level: number,
  indexInLevel: number,
): { x: number; y: number } {
  return { x: level * 360, y: indexInLevel * 130 };
}

/**
 * Строит prereq-цепочку: внутри уровня линейно (sobr → pon → sem → ont),
 * между уровнями — первая дисциплина Lk зависит от последней дисциплины
 * L(k-1). Даёт один связный путь сквозь весь стек.
 */
export function buildLevenchukNodeSeeds(): {
  slug: string;
  title: string;
  summary: string;
  positionX: number;
  positionY: number;
  estimatedMinutes: number;
  prerequisites: string[];
}[] {
  const byLevel = new Map<number, LDiscipline[]>();
  for (const d of LEVENCHUK_DISCIPLINES) {
    const bucket = byLevel.get(d.level) ?? [];
    bucket.push(d);
    byLevel.set(d.level, bucket);
  }

  const out: {
    slug: string;
    title: string;
    summary: string;
    positionX: number;
    positionY: number;
    estimatedMinutes: number;
    prerequisites: string[];
  }[] = [];

  let prevLevelLast: string | null = null;
  for (const lvl of [0, 1, 2, 3, 4, 5, 6] as const) {
    const disciplines = byLevel.get(lvl) ?? [];
    let prevInLevel: string | null = prevLevelLast;
    disciplines.forEach((d, idx) => {
      const pos = disciplinePosition(d.level, idx);
      out.push({
        slug: d.slug,
        title: d.title,
        summary: d.question,
        positionX: pos.x,
        positionY: pos.y,
        estimatedMinutes: d.estimatedMinutes,
        prerequisites: prevInLevel ? [prevInLevel] : [],
      });
      prevInLevel = d.slug;
    });
    const last = disciplines[disciplines.length - 1];
    if (last) prevLevelLast = last.slug;
  }

  return out;
}
